package com.kodewala.switchpratice;

public class Q15 {

	public static void main(String[] args) {
		for(int i=0;i<=5;i++) {
			switch (i) {
			case 1:
				System.out.println("ONE");
				break;
case 2:
	System.out.println("TWO");
				break;
case 3:
	System.out.println("THREE");
	break;
case 4:
	System.out.println("FOUR");
	break;
case 5:
	System.out.println("FIVE");
	break;

			default:
				break;
			}
		}
		

	}

}
