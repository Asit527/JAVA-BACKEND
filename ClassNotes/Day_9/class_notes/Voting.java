class Voting
{

  public static void main(String args[])
  {
   System.out.println(" My voting app!!!!");
   
   int age = 17; // Assignment operator
   
   System.out.println(" age is : "+ age);
   
   System.out.println( age > 18 ); // false
   System.out.println( age < 18 ); // true
   System.out.println( age == 18 ); // false
   System.out.println( age != 18 ); // true
  }

}