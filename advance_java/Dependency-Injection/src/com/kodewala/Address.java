package com.kodewala;

public class Address {
	private String address1Line1;
	private String address1Line2;
	private String address1Line3;
	private String city;
	private String state;
	private String pincode;
	private String country;
	
	public Address(String address1Line1, String address1Line2, String address1Line3, String city, String state,
			String pincode, String country) {
		super();
		this.address1Line1 = address1Line1;
		this.address1Line2 = address1Line2;
		this.address1Line3 = address1Line3;
		this.city = city;
		this.state = state;
		this.pincode = pincode;
		this.country = country;
	}
	
	
	

}
